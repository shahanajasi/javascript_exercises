//logical operators to check the two conditions is true or false
//1)&&(AND)
var num1 = 10;
var num2 = 30;
console.log(num1 > num2 && num1 == 10); //two conditions are equal return true if any one is false returns false

//2)OR(||)
var num1 = 10;
var num2 = 30;
console.log(num1 > num2 || num1 == 10); //if any one of the condition is true returns true or false

//3)logical not(!)
console.log(!(num1 > num2));


